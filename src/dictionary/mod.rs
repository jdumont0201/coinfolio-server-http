use std::collections::HashMap;


macro_rules! hashmap {
    ($( $key: expr => $val: expr ),*) => {{
         let mut map = ::std::collections::HashMap::new();
         $( map.insert($key, $val); )*
         map
    }}
}
#[derive(Clone,Debug)]
pub struct InfraSupra {
    pub infra: &'static str,

    pub supra: &'static str,
}

impl InfraSupra {
    pub fn to_string(&mut self) -> String {
        format!("{}{}", self.supra, self.infra)
    }
}

#[derive(Clone,Debug)]
pub  struct Dictionary {
    pub   rawToUni: HashMap<&'static str, HashMap<&'static str, InfraSupra>>,
    pub uniToRaw: HashMap<&'static str, HashMap<&'static str, HashMap<&'static str, &'static str>>>,
}

impl Dictionary {
    pub fn rawNameToUniversalName(&mut self, broker: &str, pair: &str) -> String {
        match self.rawToUni.get(&broker) {
            Some(b) => {
                match b.get(&pair) {
                    Some(mut is) => {
                        format!("{}{}", is.supra, is.infra)
                    }
                    None => pair.to_string()
                }
            }
            None => pair.to_string()
        }
    }
    pub fn infrasupraToRawName(&self, broker: &str,infra:&str,supra: &str) -> Option<String> {
        if !self.uniToRaw.contains_key(infra) {
            None
        }else  if !self.uniToRaw.get(infra).unwrap().contains_key(supra) {
            None
        }
        else if !self.uniToRaw.get(infra).unwrap().get(supra).unwrap().contains_key(broker) {
            None
        }
        else { Some(self.uniToRaw.get(infra).unwrap().get(supra).unwrap().get(broker).unwrap().to_string())
        }

    }
}

